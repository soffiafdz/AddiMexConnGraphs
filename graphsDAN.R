    for (j in seq_along(thresh1[-11])) {
        fnames[[i]][[j]] <- list.files(savedir, 
                                       sprintf('*g%i_thr%02i.*', i, j), full.names = T)
        g1DAN[[i]][[j]] <- lapply(fnames[[i]][[j]], read_rds)
